// core
var fs = require('fs')

// npm
var log2 = require('log2');

// --------------------------------------------------------------------------------------------------------------------
// set up the logger

var logStream = process.env.NODE_ENV === 'production'
    ? fs.createWriteStream('/var/log/com-javascript-minifier/app.log', { flags : 'a' })
    : process.stdout;
var log = log2({ stream : logStream })

// --------------------------------------------------------------------------------------------------------------------

module.exports = log;

